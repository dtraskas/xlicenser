package com.xinferin.model;

public class KeyRequest {
	private String email;
	private String key;
	
	public KeyRequest(){
		setEmail("");
		setKey("");
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
}
